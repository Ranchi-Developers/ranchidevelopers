<h1 class="mb-4">What is Java?</h1>
<img src="Images/javamug.png" alt="Java Logo" class="float-left mr-3" height="128px">
<p>Java is a general-purpose computer-programming language that is concurrent, class-based, object-oriented, and specifically designed to have as few implementation dependencies as possible.</p><img src="Images/duke.png" alt="Java Mascot Duke" class="float-right ml-3" height="128px"><p class="mb-5">It is intended to let application developers "Write Once, Run Anywhere" (WORA), meaning that compiled Java code can run on all platforms that support Java without the need for recompilation. Java was originally developed by James Gosling at Sun Microsystems.</p>
<h1>Feature of JAVA</h1>
<ul class="mb-5">
<li><b>Simple</b> - Java eliminates poor programming practices or rarely used: goto statements, header files, structures, operator overloading, multiple inheritance and pointers.</li><br>
<li><b>Object Oriented</b> - Java is an OOPL that supports the construction of programs that consist of collections of collaborating objects.</li><br>
<li><b>Distributed</b> - Java is designed to support various levels of network connectivity. TCP/IP support is built into Java class libraries. They can open and access remote objects on the Internet.</li><br>
<li><b>Mutlithreaded</b> - Java supports multiple threads of execution (a.k.a., lightweight processes), including a set of synchronization primitives.</li><br>
<li><b>Secure</b> - The Java run-time environment uses a bytecode verification process to ensure that code loaded over the network does not violate Java security constraints.</li><br>
<li><b>Portable</b> - JAVA programs are first compiled into bytecode, which is then interpreted using JVM (Java Virtual Machine) into machine language thus the same bytecode can be run on diffrent operating systems using the compatible JVM. This makes the program platform independent.</li><br>
<li><b>Interpreted</b> - Java byte code is translated on the fly to native machine instructions and is not stored anywhere. The development process is more rapid and analytical since the linking is an incremental and light-weight process.</li><br>
<li><b>Distributed</b> - Java is designed for the distributed environment of the internet.</li><br>
<li><b>Robust</b> - Java is strongly typed, which allows extensive compile-time error checking. It does not support memory pointers, which eliminates the possibility of overwriting memory and corrupting data.</li><br>
<li><b>High Performance</b> - With the use of Just-In-Time compilers, Java enables high performance.</li><br>
<li><b>Interpreted</b> - Java byte code is translated on the fly to native machine instructions and is not stored anywhere. The development process is more rapid and analytical since the linking is an incremental and light-weight process.</li><br>
<li><b>Architectural Neutral</b> - Java compiler generates an architecture-neutral object file format, which makes the compiled code executable on many processors, with the presence of Java runtime system.</li><br>
</ul>
